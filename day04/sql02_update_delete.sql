/*
 * UPDATE, DELETE
 * 데이터 변경, 데이터 삭제
 * */

-- 삭제
SELECT COUNT(*) FROM PROFESSOR_NEW;
SELECT * FROM PROFESSOR_NEW;

DELETE FROM PROFESSOR_NEW
  WHERE PROFNO = 1001;

-- 삭제 시 WHERE 절 빼는 것 주의!
DELETE FROM PROFESSOR_NEW; -- TRUNCATE 와 동일하나 테이블 초기화는 없음

-- 변경
UPDATE PROFESSOR_NEW SET
	ID = 'SusanS',
	PAY = 350
WHERE PROFNO = 4008;
	
/*
 * 트랜젝션, COMMIT, ROLLBACK
 * */
BEGIN TRANSACTION;

UPDATE PROFESSOR_NEW SET
	ID = 'SusanS',
	PAY = 350
WHERE PROFNO = 4008;

SELECT * FROM PROFESSOR_NEW;

ROLLBACK;
COMMIT;

